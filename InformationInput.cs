public class InformationInput
{
    public string ShopName;
    public string OwnerNumber; 
    public string OwnerName;
    public string RegistedValue;
    public InformationInput(string ShopName, string OwnerNumber, string OwnerName, string RegistedValue)
    {
        this.ShopName = ShopName;
        this.OwnerNumber = OwnerNumber;
        this.OwnerName = OwnerName;
        this.RegistedValue = RegistedValue;
    }
}